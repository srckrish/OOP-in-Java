class Datatypes {
    public static void main(String args[]) {

        int num = 9; //4 bytes
        byte by = 12; //1 byte 
        short sh = 551; //2 bytes
        long l = 50012L; //8 bytes

        float f = 5.8f; //4 bytes
        double d = 5.83; //8 bytes

        char c = 'K'; //2 bytes

        boolean b = true;

        System.out.println(num + " " + by + " " + sh + " " + l + " " + f + " " + d + " " + c + " " + b);
    }
}